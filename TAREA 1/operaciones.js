//Realizar un algoritmo en JavaScript que permita calcular la suma, resta, multiplicación y división de dos números, donde num1 = 5 y num2 = 7. Mostrar los resultados por consola.

//========================================== Suma ================================================//

let numero1 = 5;
let numero2 = 7;

const suma = numero1 + numero2;

console.log(`La suma de ${numero1} + ${numero2} es:`, suma);

//========================================== Resta ================================================//


const resta = numero1 - numero2;

console.log(`La resta de ${num1} - ${num2} es:`, resta);

//========================================== Multiplicacion ================================================//

let nu1 = 5;
let nu2 = 7;

const multiplicación = (nu1 * nu2);

console.log(`La multiplicacion de ${nu1} * ${nu2} es:`, multiplicación);

//========================================== Division ================================================//

let n1 = 5;
let n2 = 7;

const Division = n1 / n2;

console.log(`La division de ${n1}/${n2} es:`, Division);